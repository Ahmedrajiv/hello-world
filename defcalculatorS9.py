#def function
def add (a,b):
    return (a+b)
def sub (a,b):
    return (a-b)
def mul (a,b):
    return (a*b)
def div (a,b):
    return (a/b)
x= int(input("Enter X value"))
y= int(input("Enter Y value"))
print (add(x,y), "Addition")
print (sub(x,y),"Subtraction") 
print (mul(x,y),"Multiplication")
print (div(x,y),"Divsion")
    
